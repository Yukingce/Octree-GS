import torch
from torch import nn
import numpy as np
from utils.graphics_utils import getWorld2View2, getProjectionMatrix

from PIL import Image
from utils.general_utils import PILtoTorch
import cv2

class Camera(nn.Module):
    def __init__(self, colmap_id, R, T, FoVx, FoVy, resolution_scale, resolution,
                 image_path, image_name, mask_path, image_width, image_height,
                 uid, trans=np.array([0.0, 0.0, 0.0]), scale=1.0, data_device = "cuda"
                 ):
        super(Camera, self).__init__()

        self.uid = uid
        self.colmap_id = colmap_id
        self.R = R
        self.T = T
        self.FoVx = FoVx
        self.FoVy = FoVy
        self.image_name = image_name
        self.resolution_scale = resolution_scale

        try:
            self.data_device = torch.device(data_device)
        except Exception as e:
            print(e)
            print(f"[Warning] Custom device {data_device} failed, fallback to default cuda device" )
            self.data_device = torch.device("cuda")

        # self.original_image = image.clamp(0.0, 1.0).to(self.data_device)
        # self.image_width = self.original_image.shape[2]
        # self.image_height = self.original_image.shape[1]
        #
        # if gt_alpha_mask is not None:
        #     self.original_image *= gt_alpha_mask.to(self.data_device)
        # else:
        #     self.original_image *= torch.ones((1, self.image_height, self.image_width), device=self.data_device)

        self.resolution = resolution
        self.image_path = image_path
        self.image_name = image_name
        self.image_width = image_width
        self.image_height = image_height
        self.mask_path = mask_path

        self.zfar = 100.0
        self.znear = 0.01

        self.trans = trans
        self.scale = scale

        self.world_view_transform = torch.tensor(getWorld2View2(R, T, trans, scale)).transpose(0, 1).cuda()
        self.projection_matrix = getProjectionMatrix(znear=self.znear, zfar=self.zfar, fovX=self.FoVx, fovY=self.FoVy).transpose(0,1).cuda()
        self.full_proj_transform = (self.world_view_transform.unsqueeze(0).bmm(self.projection_matrix.unsqueeze(0))).squeeze(0)
        self.camera_center = self.world_view_transform.inverse()[3, :3]

    def init_gt_image(self, activate):
        if activate:
            image = Image.open(self.image_path)
            resized_image_rgb = PILtoTorch(image, self.resolution)
            self.original_image = resized_image_rgb[:3, ...].clamp(0.0, 1.0).to(self.data_device)

            gt_alpha_mask = None
            if resized_image_rgb.shape[1] == 4:
                gt_alpha_mask = resized_image_rgb[3:4, ...]

            if gt_alpha_mask is not None:
                self.original_image *= gt_alpha_mask.to(self.data_device)
            else:
                self.original_image *= torch.ones((1, self.image_height, self.image_width), device=self.data_device)

            self.valid_mask = torch.from_numpy(~cv2.imread(self.mask_path)[:, :, 0].astype(bool)).to(self.data_device) \
                if self.mask_path is not None else None
            # self.depth_image = torch.from_numpy(cv2.imread(self.depth_path, cv2.IMREAD_UNCHANGED) / 1000).to(self.data_device) \
            #     if self.depth_path is not None else None
            self.active = True

        else:
            self.active = False

    def get_deactivated_image(self):
        image = Image.open(self.image_path)
        resized_image_rgb = PILtoTorch(image, self.resolution)
        original_image = resized_image_rgb[:3, ...].clamp(0.0, 1.0).to(self.data_device)

        gt_alpha_mask = None
        if resized_image_rgb.shape[1] == 4:
            gt_alpha_mask = resized_image_rgb[3:4, ...]

        if gt_alpha_mask is not None:
            original_image *= gt_alpha_mask.to(self.data_device)
        else:
            original_image *= torch.ones((1, self.image_height, self.image_width), device=self.data_device)

        valid_mask = torch.from_numpy(~cv2.imread(self.mask_path)[:, :, 0].astype(bool)).to(self.data_device) \
            if self.mask_path is not None else None
        # depth_image = torch.from_numpy(cv2.imread(self.depth_path, cv2.IMREAD_UNCHANGED) / 1000).to(self.data_device) \
        #     if self.depth_path is not None else None

        return original_image, valid_mask

class MiniCam:
    def __init__(self, width, height, fovy, fovx, znear, zfar, world_view_transform, full_proj_transform):
        self.image_width = width
        self.image_height = height    
        self.FoVy = fovy
        self.FoVx = fovx
        self.znear = znear
        self.zfar = zfar
        self.world_view_transform = world_view_transform
        self.full_proj_transform = full_proj_transform
        view_inv = torch.inverse(self.world_view_transform)
        self.camera_center = view_inv[3][:3]

